x = [0.56 0.84 1.14 2.44 3.16];
y = [-0.8 -0.97 -0.98 1.07 3.66];
n = length(x);
A = zeros(3, 3);
b = zeros(3, 1);

for i = 1:n
    A(1, 1) = A(1, 1) + x(i)^4;
    A(1, 2) = A(1, 2) + x(i)^3;
    A(1, 3) = A(1, 3) + x(i)^2;
    
    A(2, 1) = A(2, 1) + x(i)^3;
    A(2, 2) = A(2, 2) + x(i)^2;
    A(2, 3) = A(2, 3) + x(i);
    
    A(3, 1) = A(3, 1) + x(i)^2;
    A(3, 2) = A(3, 2) + x(i);
    A(3, 3) = A(3, 3) + 1;
    
    b(1) = b(1) + x(i)^2 * y(i);
    b(2) = b(2) + x(i) * y(i);
    b(3) = b(3) + y(i);
end

heso = A \ b;


a = heso(1);
b = heso(2);
c = heso(3);

fprintf('a = %.6f, b = %.6f, c = %.6f\n', a, b, c);
